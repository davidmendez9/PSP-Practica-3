    public void borrarEmpleado()
    {
        if(lista.isFirst())
        {
            lista.removeElement();
            lista.retroceder();
            mostrarEmpleado();
        }
        else if(lista.isLast())
        {
            lista.removeElement();
            lista.avanzar();
            mostrarEmpleado();
        }
        else
        {
            lista.removeElement();
            lista.avanzar();
            mostrarEmpleado();
        }
            if(lista.isFirst() && lista.isLast())
            {
                botonSiguiente.setEnabled(false);
                botonAnterior.setEnabled(false);
                buttonBorrar.setEnabled(false);
            }
            
    }
    
    public void mostrarEmpleado()
    {
        Empleado e1 = (Empleado) lista.getActualNode().getMain();
        numeroF.setText(""+ e1.getNum());
        nombreF.setText(e1.getNombre());
        fechaAltaDia.setText(""+ e1.getFechaAlta().getDia());
        fechaAltaMes.setText(""+ e1.getFechaAlta().getMes());
        fechaAltaAnio.setText(""+ e1.getFechaAlta().getAnio());
        sueldoF.setText(""+ e1.getSueldo());
        edadF.setText("" + e1.getEdad());
    }
    
    public void crearEmpleado()
    {
        
        
        if((Integer.parseInt(fechaAltaDia.getText()) < 1 || Integer.parseInt(fechaAltaDia.getText()) > 31) && (Integer.parseInt(fechaAltaMes.getText()) < 1 || Integer.parseInt(fechaAltaMes.getText()) > 12))
        {
           JOptionPane.showMessageDialog(this, "DÃ­a y mes introducidos no son vÃ¡lidos"); 
        }
        else
        {
            if(Integer.parseInt(fechaAltaDia.getText()) < 1 || Integer.parseInt(fechaAltaDia.getText()) > 31)
            {
                JOptionPane.showMessageDialog(this, "El dÃ­a de alta introducido no es vÃ¡lido.");
            }
            else
            {
                if(Integer.parseInt(fechaAltaMes.getText()) < 1 || Integer.parseInt(fechaAltaMes.getText()) > 12)
                {
                    JOptionPane.showMessageDialog(this, "El mes de alta introducido no es vÃ¡lido.");
                }
                else
                {
                    if(aceptarMode == false)
                    {
                        lista.add(new Empleado(Integer.parseInt(numeroF.getText()), nombreF.getText(), Integer.parseInt(fechaAltaDia.getText()), Integer.parseInt(fechaAltaMes.getText()), Integer.parseInt(fechaAltaAnio.getText()), Integer.parseInt(sueldoF.getText()), Integer.parseInt(edadF.getText())));
                        botonAnterior.setEnabled(false);
                    }
                    else
                    {
                        
                        lista.getActualNode().setMain(new Empleado(Integer.parseInt(numeroF.getText()), nombreF.getText(), Integer.parseInt(fechaAltaDia.getText()), Integer.parseInt(fechaAltaMes.getText()), Integer.parseInt(fechaAltaAnio.getText()), Integer.parseInt(sueldoF.getText()), Integer.parseInt(edadF.getText())));
                    }
                }
            }
        }
    }
    
    
